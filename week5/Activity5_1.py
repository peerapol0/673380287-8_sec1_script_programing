# 1. Dictionary ในเกม
# ตัวอย่าง: ช่องเก็บของของผู้เล่น (Inventory)
# - ใช้ Dictionary เพื่อเก็บชื่อไอเท็มเป็น key และจำนวนไอเท็มเป็น value
# - เหตุผล: เราต้องการเข้าถึงข้อมูลไอเท็มได้รวดเร็ว โดยใช้ชื่อไอเท็มเป็นคำอ้างอิง
#   เช่น {"health_potion": 3, "mana_potion": 2, "sword": 1}
# - ตัวอย่างเพิ่มเติม: เก็บค่าพลังของตัวละคร
#   เช่น {"hp": 100, "mana": 80, "attack": 25}

# 2. Dictionary ในร้านค้าออนไลน์
# ตัวอย่าง: เก็บข้อมูลสินค้าโดยใช้รหัสสินค้า (Product ID) เป็น key
# - ใช้ Dictionary เพื่อเก็บรายละเอียดสินค้าอย่างเป็นระเบียบ
# - เหตุผล: รหัสสินค้าจะช่วยให้เราค้นหาข้อมูลสินค้าได้เร็วและไม่ซ้ำกัน
#   เช่น {
#       "P001": {"name": "เสื้อยืด", "price": 250, "stock": 10},
#       "P002": {"name": "กางเกง", "price": 400, "stock": 5}
#   }

# 3. Set ในการจัดการสิทธิ์ของผู้ใช้
# ตัวอย่าง: เก็บสิทธิ์ของผู้ใช้แบบไม่ซ้ำกัน
# - ใช้ Set เพื่อเก็บบทบาทหรือสิทธิ์ที่ไม่ซ้ำกัน
# - เหตุผล: Set เหมาะสำหรับสถานการณ์ที่ต้องการตรวจสอบว่า สิทธิ์นี้มีอยู่หรือไม่
#   โดยไม่สนใจลำดับและไม่ต้องการข้อมูลซ้ำ
#   เช่น {"admin", "editor", "viewer"}

# 4. Set ในการจัดการแท็ก
# ตัวอย่าง: แฮชแท็กของโพสต์หรือข้อความ
# - ใช้ Set เพื่อเก็บแท็กที่ไม่ซ้ำกัน
# - เหตุผล: แท็กแต่ละอันควรมีความเป็นเอกลักษณ์และไม่ซ้ำกัน
#   เช่น {"#game", "#study", "#python"}

# 5. สถานการณ์ที่ต้องเช็กว่า “ข้อมูลนี้อยู่ในกลุ่มข้อมูลขนาดใหญ่หรือไม่”
# ตัวอย่าง: รายชื่อ IP Address ที่ถูกบล็อก
# - ใช้ Set เพื่อเช็กว่า IP Address ที่ผู้ใช้พยายามเข้าถึงอยู่ในรายการที่ถูกบล็อกหรือไม่
# - เหตุผล: Set มีความเร็วสูงในการตรวจสอบการเป็นสมาชิก
#   เช่น blocked_ips = {"192.168.1.10", "10.0.0.5", "203.0.113.22"}
#   ถ้าเช็กว่า "192.168.1.10" อยู่ใน blocked_ips หรือไม่ จะทำได้รวดเร็วมาก

# 6. สรุปเหตุผลที่ Dictionary และ Set เหมาะสม
# - Dictionary เหมาะสำหรับข้อมูลที่ต้องการอ้างอิงด้วย key และเก็บค่าที่สัมพันธ์กัน
# - Set เหมาะสำหรับข้อมูลที่ต้องการเก็บสิ่งที่ไม่ซ้ำกันและเช็กความเป็นสมาชิกอย่างรวดเร็ว
